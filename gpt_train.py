import torch
import matplotlib.pyplot as plt
import numpy as np
from tqdm import tqdm

def train_model(model, train_loader, val_loader, optimizer, device, num_epochs,
                eval_freq, eval_iter, start_context, tokenizer):
    # Initialize lists to track losses and tokens seen
    train_losses, val_losses, track_tokens_seen = [], [], []
    tokens_seen = 0
    global_step = -1
    
    # Calculate total batches for better progress estimation
    total_batches = len(train_loader) * num_epochs

    # Main training loop with epoch progress bar
    for epoch in range(num_epochs):
        model.train()
        
        # Create progress bar for batches in this epoch
        pbar = tqdm(train_loader, 
                    desc=f"🔄 Epoch {epoch+1}/{num_epochs}", 
                    leave=False, 
                    colour='green',
                    bar_format='{l_bar}{bar:20}{r_bar}{bar:-10b}')

        for batch_idx, (input_batch, target_batch) in enumerate(pbar, 1):
            optimizer.zero_grad()
            loss = calc_loss_batch(input_batch, target_batch, model, device)
            loss.backward()
            optimizer.step()
            
            tokens_seen += input_batch.numel()
            global_step += 1

            # Update progress bar with metrics
            pbar.set_postfix({
                'loss': f'{loss.item():.3f}',
                'tokens': f'{tokens_seen:,}',
                'step': f'{global_step}'
            })

            # Optional evaluation step
            if global_step % eval_freq == 0:
                model.eval()
                with torch.no_grad():
                    train_loss, val_loss = evaluate_model(
                        model, train_loader, val_loader, device, eval_iter)
                train_losses.append(train_loss)
                val_losses.append(val_loss)
                track_tokens_seen.append(tokens_seen)
                model.train()
                
                # Print evaluation results with emoji
                print(f"\n📊 Evaluation @ Step {global_step:06d}")
                print(f"   ├─ Train loss: {train_loss:.3f}")
                print(f"   └─ Val loss:   {val_loss:.3f}")

        # Epoch complete message
        print(f"\n✅ Epoch {epoch+1}/{num_epochs} completed!")

    return train_losses, val_losses, track_tokens_seen

def calc_loss_batch(input_batch, target_batch, model, device):
    input_batch, target_batch = input_batch.to(device), target_batch.to(device)
    logits = model(input_batch)
    loss = torch.nn.functional.cross_entropy(logits.flatten(0, 1), target_batch.flatten())
    return loss

def calc_loss_loader(data_loader, model, device, num_batches=None, show_progress=False):
    total_loss = 0.    
    if len(data_loader) == 0:
        return float("nan")
    elif num_batches is None:
        num_batches = len(data_loader)
    else:
        num_batches = min(num_batches, len(data_loader))
    
    # Create progress bar if requested
    if show_progress:
        pbar = tqdm(
            enumerate(data_loader), 
            total=num_batches, 
            desc="📊 Evaluating", 
            leave=False, 
            colour='blue',
            bar_format='{l_bar}{bar:20}{r_bar}{bar:-10b}'
        )
    else:
        pbar = enumerate(data_loader)
    
    for i, (input_batch, target_batch) in pbar:
        if i < num_batches:
            loss = calc_loss_batch(input_batch, target_batch, model, device)
            total_loss += loss.item()
            
            # Update progress bar with current loss
            if show_progress:
                pbar.set_postfix({'loss': f'{loss.item():.3f}'})
        else:
            break
    
    # Close progress bar if it was created
    if show_progress:
        pbar.close()
    
    return total_loss / num_batches

def evaluate_model(model, train_loader, val_loader, device, eval_iter, show_progress=False):
    model.eval()
    with torch.no_grad():
        train_loss = calc_loss_loader(
            train_loader, model, device, 
            num_batches=eval_iter,
            show_progress=show_progress
        )
        val_loss = calc_loss_loader(
            val_loader, model, device, 
            num_batches=eval_iter,
            show_progress=show_progress
        )
    model.train()
    return train_loss, val_loss

